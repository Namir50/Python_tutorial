#range function has three parameters: start,end,jump
#start default is zero
#jump default is 1

#without start and jump
x = list(range(10))
print(x)

#with start and not jump
y = list(range(2,10))
print(y)

#with start and jump
z = list(range(2,10,2))
print(z)

a = list(range(10,1,-1))
print(a)












