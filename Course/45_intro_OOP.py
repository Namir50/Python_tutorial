#The principles of object oriented programming are based on some pillars which are class, objects, Inheritance, Polymorphism, Abstraction and Encapsulation
#A class has attributes and behaviours

#Abstraction means displaying only important and essential details and hiding other details
#Example: Car will accelerate but internal working of engine is not exposed

#Encapsulation means bundling of data into a single unit
#Bundling of all methods that can act on an object of that data
#Example: car can have color, brake, engine and a lot of other methods

#Inheritance means when a class derives or inherit properties from other class
#Example: A new car company can inherit properties of the existing car class

#Polymorphism means hsving many forms
#Same class method can work differently for different objects